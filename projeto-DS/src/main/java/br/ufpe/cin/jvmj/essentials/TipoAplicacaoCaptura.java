package br.ufpe.cin.jvmj.essentials;

public class TipoAplicacaoCaptura {
	
	private int id;
	private String name;
	
	public TipoAplicacaoCaptura(String name) {
		this.name = name;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
